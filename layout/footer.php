<script>
    function activeTab(obj){
		$('.product-tab ul li').removeClass('active');
		$(obj).addClass('active');
		var id = $(obj).attr('data-tab');
		$('.tab-content').removeClass('active');
		$(id).addClass('active');
	}


	$('.product-tab ul li').click(function(){
		activeTab(this);
		return false;
	});


    $('.btn--view-more').on('click', function(e){
		e.preventDefault();
		var $this = $(this);
		if( $('.l_content_detl .product-review-content').hasClass('expanded')) {
			$('html, body').animate({ scrollTop: $('.l_content_detl').offset().top - 110 }, 'slow');
		}
		$this.parents('.l_content_detl').find('.product-review-content').toggleClass('expanded');
		$(this).toggleClass('active');
		return false;
	});
</script>

